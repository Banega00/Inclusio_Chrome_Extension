function keyPress(e) {
    var evtobj = window.event? event : e

    
    if (evtobj.keyCode == 90 && evtobj.ctrlKey){//CTRL + Z
        getUser()
        .then(user =>{
            const currentPageUrl = trimQueryParamsFromUrl(window.location.href);
            chrome.storage.sync.get('ext-status', function (result) {
                let extStatus = result['ext-status'];
    
                if (extStatus == undefined) { //extension status is totally empty
                    extStatus = {
                        [currentPageUrl]: true
                    }
                    launchSound_turned_on();
                    chrome.storage.sync.set({ 'ext-status': extStatus }, () => extensionStatusChange(extStatus[currentPageUrl], user?.role, true))
                } else {
                    if (typeof extStatus != 'object') extStatus = {};
    
                    const currentPageExtStatus = extStatus[currentPageUrl];
    
                    if (currentPageExtStatus == undefined) {
    
                        extStatus[currentPageUrl] = true;
                        launchSound_turned_on();
                        chrome.storage.sync.set({ 'ext-status': extStatus }, () => extensionStatusChange(extStatus[currentPageUrl], user?.role, true))
                        return;
                    } else {
                        extStatus[currentPageUrl] = !currentPageExtStatus

                        if(extStatus[currentPageUrl]){
                            launchSound_turned_on();
                        }else{
                            launchSound_turned_off();
                        }
                        chrome.storage.sync.set({ 'ext-status': extStatus }, () => extensionStatusChange(extStatus[currentPageUrl], user?.role, true))
                        return;
                    }
    
                }
            })
        })
    }else if(evtobj.keyCode == 88 && evtobj.ctrlKey){//CTRL + X
        const currentPageUrl = trimQueryParamsFromUrl(window.location.href);
        const pageTitle = document.title;
        
        chrome.storage.sync.get('ext-status', function (result) {
            let extStatus = result['ext-status'];

            if(extStatus[currentPageUrl] == true){
                requestPage(currentPageUrl, pageTitle);

            }
        })
    }
}

function launchSound_turned_on(){
    let audio = new Audio(`${backend_url}/assets/sounds/on.mp3`);
    audio.play();
}

function launchSound_turned_off(){
    let audio = new Audio(`${backend_url}/assets/sounds/off.mp3`);
    audio.play();
}

function requestPage(pageUrl, pageTitle){
    chrome.storage.sync.get('user', function (result) {
        const user = result.user;
        if(!user || !user.token) return;

        fetch(`${backend_url}/request-page`,{
            method: 'POST',
            body: JSON.stringify({ pageUrl, pageTitle }),
            headers:{
                Authorization: user.token,
                'Content-Type': 'application/json'
            }
        })
        .then(response =>{
            if(response.status == 200){
                showExtensionStatusMessage(true, 'Page requested for processing')
            }
        })
        .catch(error => {
            console.log(error)
        })
    })
}

document.onkeydown = keyPress;